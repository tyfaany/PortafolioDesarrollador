export function formatGithubRelativeTime(dias) {
  if (dias <= 1) {
    return "hace 1 día";
  }

  if (dias < 30) {
    return `hace ${dias} días`;
  }

  const meses = Math.round(dias / 30);
  return `hace ${meses} ${meses === 1 ? "mes" : "meses"}`;
}
